let saleDay = "blackfriday";

/* if...else */
if (saleDay === "blackfriday") {
  console.log("Giảm 50% toàn bộ sản phẩm!");
} else if (saleDay === "cybermonday") {
  console.log("Giảm 40% cho sản phẩm công nghệ!");
} else if (saleDay === "christmas") {
  console.log("Ưu đãi đặc biệt mùa Noel - giảm 30%!");
} else {
  console.log("Không có khuyến mãi cho ngày này.");
}